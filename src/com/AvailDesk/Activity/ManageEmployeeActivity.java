    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         if(jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals(""))
       {
           JOptionPane.showMessageDialog(null, "All fields required*", "Error!", JOptionPane.ERROR_MESSAGE);
       }
         else
         {
        if(jTable1.getSelectionModel().isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(null, "Select any row to update!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        else
        {

            DefaultTableModel tblmodel = (DefaultTableModel)jTable1.getModel();
            String id = tblmodel.getValueAt(jTable1.getSelectedRow(), 0).toString();
            String username = jTextField2.getText();  
            String password = jTextField3.getText();
            String email = jTextField4.getText();
           
            if(UserController.updateEmployee(id, username,email,password))
            {
                loadEmployee();
                JOptionPane.showMessageDialog(null, "Successfully Employee Updated!", "Success!", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Something went wrong!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
         }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(jTable1.getSelectionModel().isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(null, "Select any row to delete!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        else
        {

            DefaultTableModel tblmodel = (DefaultTableModel)jTable1.getModel();
            String id = tblmodel.getValueAt(jTable1.getSelectedRow(), 0).toString();

        
            if(UserController.deleteEmployee(id))
            {
                loadEmployee();
                JOptionPane.showMessageDialog(null, "Successfully Employee Deleted!", "Success!", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Something went wrong!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

